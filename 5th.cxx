#include<iostream>
using namespace std;
int main()
{ 
    int a;
    cout<<"enter a number = ";
    cin>>a; 
    a+=5;
    cout<<"the value of a after a+=5 = "<<a<<endl;
    a-=4;
    cout<<"the value of a after a-=4 = "<<a<<endl;
    a*=6;
    cout<<"the value of a after a*=6 = "<<a<<endl;
    a/=5;
    cout<<"the value of a after a/=5 = "<<a<<endl;
    a++;
    cout<<"the value of a after a++ = "<<a<<endl;
    a--;
    cout<<"the value of a after a-- = "<<a;
}
